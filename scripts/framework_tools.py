def _render_template(content: str, replacements: dict[str, str]) -> str:
    rendered = content
    while True:
        updated = rendered
        for key, value in replacements.items():
            updated = updated.replace(f"{{{{{key}}}}}", value)
        if updated == rendered:
            return updated
        rendered = updated


