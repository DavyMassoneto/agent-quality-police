def _check_local_links(root: Path, path: Path, content: str) -> list[str]:
    errors: list[str] = []
    for _, target in LOCAL_LINK_PATTERN.findall(content):
        if target.startswith(("http://", "https://", "mailto:", "#")):
            continue
        normalized_target = target.split("#", 1)[0]
        candidate = (path.parent / normalized_target).resolve()
        if not candidate.exists():
            errors.append(f"Broken link in {path.relative_to(root)} -> {target}")
    for include_target in AT_INCLUDE_PATTERN.findall(content):
        candidate = (path.parent / include_target).resolve()
        if not candidate.exists():
            errors.append(f"Broken include in {path.relative_to(root)} -> @{include_target}")
    return errors


def _is_entrypoint_template(path: Path, content: str) -> bool:
    return "framework" in path.parts and "entrypoints" in path.parts and bool(ENTRYPOINT_TEMPLATE_PATTERN.search(content))


def validate_repository(root: Path) -> ValidationResult:
    errors: list[str] = []
    warnings: list[str] = []

    required_files = [
        root / "README.md",
        root / "AGENTS.md",
        root / "docs" / "policy" / "quality-definition.md",
    ]
    for path in required_files:
        if not path.exists():
            errors.append(f"Missing required file: {path.relative_to(root)}")

    for path in _iter_text_files(root):
        content = path.read_text(encoding="utf-8")
        if PLACEHOLDER_PATTERN.search(content):
            errors.append(f"Placeholder token found in {path.relative_to(root)}")
        if path.suffix in {".md", ".json", ".toml"} and (
            "REPO_NAME=" in content or "REPO_DESCRIPTION=" in content
        ):
            errors.append(f"Unresolved repository placeholder found in {path.relative_to(root)}")
        if (path.suffix == ".md" or path.name == "SKILL.md") and not _is_entrypoint_template(path, content):
            errors.extend(_check_local_links(root, path, content))

    forbidden_root_paths = [
        root / ".claude",
        root / ".codex",
        root / ".opencode",
        root / ".agents",
        root / ".claude-plugin",
        root / "CLAUDE.md",
        root / "opencode.json",
    ]
    for forbidden_path in forbidden_root_paths:
        if forbidden_path.exists():
            errors.append(
                f"Tool-specific runtime path must not exist at repository root: {forbidden_path.relative_to(root)}"
            )

    expected_entrypoint_projections = _expected_entrypoint_projections(root)
    for relative_path, expected_content in expected_entrypoint_projections.items():
        actual_path = root / relative_path
        if not actual_path.exists():
            errors.append(f"Missing generated entrypoint file: {relative_path}")
            continue
        actual_content = actual_path.read_text(encoding="utf-8")
        if actual_content != expected_content:
            errors.append(
                "Generated entrypoint drift between canonical entrypoint sources and "
                f"{relative_path}"
            )

    expected_plugin_distribution = _expected_plugin_distribution(root)
    for relative_path, expected_content in expected_plugin_distribution.items():
        actual_path = root / relative_path
        if not actual_path.exists():
            errors.append(f"Missing generated plugin distribution file: {relative_path}")
            continue
        actual_content = actual_path.read_text(encoding="utf-8")
        if actual_content != expected_content:
            errors.append(
                "Generated plugin distribution drift between canonical sources and "
                f"{relative_path}"
            )

    return ValidationResult(errors=errors, warnings=warnings)


def build_all(root: Path) -> tuple[list[str], list[str], list[str], list[str]]:
    entrypoint_policy = _load_entrypoint_policy(root, required=True)
    assert entrypoint_policy is not None
    source_skills = _load_skill_sources(root, required=True)
    specs = _load_agent_specs(root, required=True)
    distribution_spec = _load_distribution_spec(root, required=True)
    entrypoints = build_entrypoint_projections(root, entrypoint_policy)
    skills = build_skill_projection(root, source_skills)
    agents = build_agent_projections(root, specs)
    distributions = build_plugin_distribution(root, distribution_spec)
    return entrypoints, skills, agents, distributions


def _default_root() -> Path:
    return Path(__file__).resolve().parent.parent


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Build or validate the agent governance framework.")
    parser.add_argument("command", choices=("build", "validate"))
    parser.add_argument("--root", default=str(_default_root()))
    args = parser.parse_args(argv)

    root = Path(args.root).resolve()
    if args.command == "build":
        try:
            entrypoints, skills, agents, distributions = build_all(root)
        except BuildError as error:
            print(f"ERROR: {error}")
            return 1
        print(
            " ".join(
                [
                    f"built-entrypoints={len(entrypoints)}",
                    f"built-skills={len(skills)}",
                    f"built-agents={len(agents)}",
                    f"built-distributions={len(distributions)}",
                ]
            )
        )
        return 0

    result = validate_repository(root)
    for warning in result.warnings:
        print(f"WARNING: {warning}")
    for error in result.errors:
        print(f"ERROR: {error}")
    if result.errors:
        return 1
    print("validation-ok")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
